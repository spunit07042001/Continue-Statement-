public class continueStatement {
    public static void main(String args[]) {
        // Scanner sc = new Scanner (System.in);

        for (int i=1; i<=5; i++)
        {
            if (i==3)// skit 4 from the loop
            {
                continue;
            }
            System.out.println(i);
        }
        
    }
    
}
